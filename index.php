      <h3>Music Theory for Dummies<sup>1</sup></h3>
        <p><q>Traditionally, when you first begin taking piano lessons&mdash;especially classical piano&mdash;you’re not really taught much about key signatures beyond remembering the one or two sharps or flats you have to play in the piece of sheet music set in front of you. You’re taught to play the notes exactly as they occur in that piece, without much thought about what relationship those notes have with one another in the key.</q></p>
        <p><q>Conversely, guitar players (even bass guitar players) are almost immediately taught about chords, scales, and finding their way around the guitar neck intuitively in their lessons. Note reading is saved for more advanced students who are already familiar with all the sounds of their instrument and where those sounds are located.<q></p>
        <p><q>This situation is why a lot of classically trained pianists have such a hard time learning how to improvise, and why guitarists and bassists who may not even know how to read music have an edge over such students when it comes to improvising. Knowing which notes go into a specific key is essential when you’re playing with other musicians, or even if you’re noodling around on your own without the help of sheet music.</q></p>
