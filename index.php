<section id="circleDefined">

  <h2>The Circle of 5ths Defined</h2>

  <div class="">

    <article>
      <h3>Wikipedia<sup>2</sup></h3>
      <p><q>In music theory, the circle of fifths is a visual representation of the relationships among the 12 tones of the chromatic scale, their corresponding key signatures, and the associated major and minor keys. More specifically, it is a geometrical representation of relationships among the 12 pitch classes of the chromatic scale in pitch class space.</q></p>
      <p><q>A simple way to hear the relationship between these notes is by playing them on a piano keyboard. If you traverse the circle of fifths backwards, the notes will feel as though they fall into each other. This aural relationship is what the mathematics describe.</q></p>
    </article>

    <article>
      <h3>Harvard Dictionary of Music<sup>3</sup></h3>
        <p><q>The circular, clockwise arrangement of the twelve keys in an order of ascending fifths (C, D, d, a, etc.), showing that after twelve such steps the initial key is reached again. In this order, each successive key increases by one the number of sharps in the key signature. If the circle is viewed counter-clockwise (i.e., order of descending fifths: c, f, b♭, etc.), the keys follow each other with one or more flat in the signature. At one point of the circle the transition from the sharp keys to the flat keys must be made.</q></p>
    </article>

  </div>

  <div>

    <article>
      <h3>Norton/Grove Encyclopedia of Music<sup>4</sup></h3>
        <p><q>The arrangement of the tonics of the 12 major or minor keys by ascending or descending perfect 5ths, making a closed circle:</q></p>
        <p><q>C-G-D-A-E-B-F♯ = G♭-D♭-A♭-E♭-B♭-F-C.</q></p>
    </article>

    <article>
      <h3>Oxford Dictionary of Music<sup>5</sup></h3>
        <p><q>Used by theorists to illustrate relative harmonic remoteness of one key from another, i.e. the number of fifths by which 2 notes are separated along the circle. If arranged clockwise by ascending (or descending) perfect fifths, the tonic of the 12 major (or minor) keys make a closed circle.</q></p>
    </article>

  </div>

